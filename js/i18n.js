let currentLang = localStorage.getItem('nmnh-lang') || 'de';

function t(key) {
  return (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key]) || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translation = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = translation;
    } else {
      el.textContent = translation;
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.getAttribute('data-i18n-html'));
  });

  document.documentElement.lang = currentLang;

  const langFlag = document.getElementById('langFlag');
  const langText = document.getElementById('langText');
  if (langFlag) langFlag.textContent = currentLang === 'de' ? '🇩🇪' : '🇬🇧';
  if (langText) langText.textContent = currentLang === 'de' ? 'DE' : 'EN';

  const canonicalEl = document.querySelector('link[rel="canonical"]');
  if (canonicalEl) {
    const basePath = canonicalEl.getAttribute('data-base') || canonicalEl.href;
    if (currentLang === 'en') {
      canonicalEl.href = basePath.replace('https://www.nomoneynohoney.io', 'https://www.nomoneynohoney.io/en');
    }
  }
}

function toggleLang() {
  currentLang = currentLang === 'de' ? 'en' : 'de';
  localStorage.setItem('nmnh-lang', currentLang);

  const els = document.querySelectorAll('[data-i18n], [data-i18n-placeholder], [data-i18n-html]');
  els.forEach(el => el.style.transition = 'opacity 0.2s ease');
  els.forEach(el => el.style.opacity = '0');

  setTimeout(() => {
    applyTranslations();
    els.forEach(el => el.style.opacity = '1');
  }, 200);
}

document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();

  const langBtn = document.getElementById('langToggle');
  if (langBtn) langBtn.addEventListener('click', toggleLang);

  const mobileLangBtn = document.getElementById('mobileLangToggle');
  if (mobileLangBtn) mobileLangBtn.addEventListener('click', toggleLang);
});
